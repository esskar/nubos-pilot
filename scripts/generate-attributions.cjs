#!/usr/bin/env node
'use strict';

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'ATTRIBUTIONS.md');
const pkg = require(path.join(ROOT, 'package.json'));

function cleanUrl(url) {
  if (!url) return '';
  let out = String(url)
    .replace(/^git\+/, '')
    .replace(/^git:\/\//, 'https://')
    .replace(/\.git$/, '');
  const shorthand = out.match(/^(github|gitlab|bitbucket):(.+)$/);
  if (shorthand) {
    const host = { github: 'github.com', gitlab: 'gitlab.com', bitbucket: 'bitbucket.org' }[shorthand[1]];
    out = `https://${host}/${shorthand[2]}`;
  }
  return out;
}

function readInstalled(name) {
  try {
    const meta = require(path.join(ROOT, 'node_modules', name, 'package.json'));
    let license = meta.license;
    if (!license && Array.isArray(meta.licenses) && meta.licenses[0]) license = meta.licenses[0].type;
    const repo = meta.repository
      ? (typeof meta.repository === 'string' ? meta.repository : meta.repository.url)
      : meta.homepage;
    return { version: meta.version || '', license: license || 'UNKNOWN', source: cleanUrl(repo) };
  } catch {
    return null;
  }
}

function collect(deps, kind) {
  const rows = [];
  for (const name of Object.keys(deps || {}).sort()) {
    const info = readInstalled(name);
    if (info) {
      rows.push({ name, kind, range: deps[name], version: info.version, license: info.license, source: info.source });
    } else {
      rows.push({ name, kind, range: deps[name], version: '', license: '(not installed)', source: '' });
    }
  }
  return rows;
}

function escapeMd(s) {
  return String(s).replace(/\|/g, '\\|');
}

function renderRow(r) {
  const src = r.source ? `<${r.source}>` : '—';
  const version = r.version || r.range || '—';
  return `| \`${escapeMd(r.name)}\` | ${r.kind} | ${escapeMd(version)} | ${escapeMd(r.license)} | ${src} |`;
}

function render() {
  const runtime = collect(pkg.dependencies, 'runtime');
  const optional = collect(pkg.optionalDependencies, 'optional');
  const rows = runtime.concat(optional);
  const out = [];
  out.push('# Attributions');
  out.push('');
  out.push(`nubos-pilot itself is licensed under [LICENSE](./LICENSE). It builds on the third-party packages below, each under its own license. Optional packages ship only when a user opts into the feature that needs them (the Vector-Memory layer, [ADR-0014](https://pilot.nubos.cloud/v1/adr/0014-vector-memory-layer)).`);
  out.push('');
  out.push('This file is generated by `scripts/generate-attributions.cjs` from `package.json` and the installed `node_modules`. Regenerate with `npm run attributions`; verify with `npm run attributions:check`.');
  out.push('');
  out.push('| Package | Type | Version | License | Source |');
  out.push('|---|---|---|---|---|');
  for (const r of rows) out.push(renderRow(r));
  out.push('');
  return out.join('\n');
}

function main() {
  const check = process.argv.includes('--check');
  const body = render();
  if (check) {
    let current = '';
    try { current = fs.readFileSync(OUT, 'utf-8'); } catch {}
    if (current !== body) {
      process.stderr.write('ATTRIBUTIONS.md is out of date. Run: npm run attributions\n');
      process.exit(1);
    }
    process.stdout.write('[ok] ATTRIBUTIONS.md up to date\n');
    return;
  }
  fs.writeFileSync(OUT, body);
  process.stdout.write('[ok] wrote ATTRIBUTIONS.md\n');
}

if (require.main === module) main();

module.exports = { render };
